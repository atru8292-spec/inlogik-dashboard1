import { NextResponse } from "next/server";

export const dynamic = "force-dynamic";
export const maxDuration = 30;

export async function POST(request: Request) {
  try {
    const { quotes, requestInfo } = await request.json();

    if (!quotes || quotes.length === 0) {
      return NextResponse.json({ error: "Нет ставок для анализа" }, { status: 400 });
    }

    const quotesText = quotes.map((q: any, i: number) => {
      const priceRaw = `${q.price} ${q.currency}`;
      const priceUSD = q.estimated_total ? `≈ $${Number(q.estimated_total).toLocaleString("ru")}` : "";
      const transit = q.transit_days
        ? `${q.transit_days} дн`
        : q.transit_days_min && q.transit_days_max
        ? `${q.transit_days_min}–${q.transit_days_max} дн`
        : "срок не указан";
      const included = q.included?.length ? q.included.join(", ") : "не указано";
      const excluded = q.excluded?.length ? q.excluded.join(", ") : "—";
      const warnings = q.hidden_cost_warnings?.length ? q.hidden_cost_warnings.join("; ") : "—";
      const valid = q.valid_until ? `до ${q.valid_until}` : "не указана";
      const notes = q.notes || q.summary_human || "";

      return `[${i + 1}] ${q.contractor?.name || "Неизвестный"}
  Цена: ${priceRaw}${priceUSD ? " " + priceUSD : ""}
  Транзит: ${transit}
  Условия (инкотермс): ${q.incoterms || "не указаны"}
  Контейнер/тип: ${q.container_type || "не указан"}
  Включено: ${included}
  НЕ включено: ${excluded}
  Скрытые расходы / предупреждения: ${warnings}
  Валидность ставки: ${valid}${notes ? "\n  Примечание: " + notes : ""}`;
    }).join("\n\n");

    const systemPrompt = `Ты — старший логист с 10+ годами опыта в международных грузоперевозках (Китай, Европа, Турция → Россия). 
Ты помогаешь менеджерам выбрать лучшую ставку из нескольких предложений.

ГЛАВНЫЙ ПРИОРИТЕТ — минимальная итоговая стоимость доставки "под ключ" для клиента.
Не просто самая низкая ставка в письме, а самая низкая РЕАЛЬНАЯ стоимость с учётом всего что придётся доплатить.
Если дешёвая ставка скрывает кучу доп расходов — она может оказаться дороже "дорогой" ставки где всё включено.
Всегда считай итоговую стоимость = ставка + DTHC + таможня + последняя миля + доставка от порта + всё что не включено.

ОБЯЗАТЕЛЬНО: перед рекомендацией посчитай примерный итог для КАЖДОЙ ставки и сравни их между собой.
Только после этого выбирай лучшую. Не выбирай ставку просто потому что у неё указан срок или она выглядит "полной" — считай деньги.

Твой подход к анализу:

ЦЕНА:
- Сравнивай только сопоставимые ставки. Если ставки в разных валютах — приводи к USD по примерному курсу (CNY/7, EUR×1.08, RUB/90).
- Если у ставки нет estimated_total — это красный флаг, реальная стоимость может быть выше.
- Учитывай что "дешёвая" ставка может стать дорогой если не включены DTHC, таможня, последняя миля.

УСЛОВИЯ:
- EXW = забор с завода не включён, это +$200-500 обычно.
- FOB = отправитель сам везёт до порта.
- DTHC в порту назначения — почти всегда доп расход $400-800 если не включён.
- "Последняя миля" до склада/двери — часто не включена, уточни.
- Таможенное оформление в РФ — если не включено, это ещё +$300-600.

СРОКИ:
- Если срок не указан — это риск. Приоритет тем кто указал конкретный срок.
- ЖД Китай-Россия обычно 18-30 дней, авто 15-25 дней, море через Владик 25-40 дней, через Новороссийск 30-45 дней.
- Если указан порт Владивосток — не забудь что нужна доставка до конечного города (ещё 5-10 дней и $500-1500).

РАЗНЫЕ ПОРТЫ — ОЧЕНЬ ВАЖНО:
- Ставки через Владивосток и через Новороссийск НЕЛЬЗЯ сравнивать напрямую.
- Ставка "море до Владивостока" — это ТОЛЬКО фрахт. К ней ВСЕГДА надо прибавить ЖД Владивосток-Москва ≈ $2,500-3,500 за 40HC (или 250,000-350,000 руб) плюс терминальные расходы $500-800. Итого доп расходы от Владика ≈ $3,000-4,300.
- Ставка "море до Новороссийска" — порт ближе к Москве, доставка дешевле ≈ $500-1,500.
- Ставка "ЖД до Москвы" или "до Белого Раста/Ворсино" — финальная, доп доставка минимальна $200-500.
- Если одна ставка $3,600 до Владика, а другая $8,250 до Москвы — реальная итоговая стоимость первой будет $3,600 + $3,500 (ЖД) + $600 (терминал) = $7,700, что ДЕШЕВЛЕ второй.
- Если видишь что подрядчики предлагают разные порты — обязательно посчитай итог для каждого варианта.

СТРУКТУРА ТВОЕГО ОТВЕТА (строго такая, без markdown):

РЕКОМЕНДУЮ: [название] — [цена с валютой] (≈ [в USD если другая валюта])

Почему: напиши 3-4 конкретных предложения. Объясни цену, что включено, срок, надёжность подрядчика. Говори как коллега — просто и по делу.

На что обратить внимание: перечисли конкретные риски и скрытые расходы этой ставки. Что нужно уточнить у подрядчика перед подтверждением. Если ставка чистая — так и скажи.

Остальные варианты:
[название] — [цена] — одна фраза почему не выбрал (дороже, нет срока, скрытые расходы, ненадёжный и т.д.)
[название] — [цена] — одна фраза
...

Итого к доплате сверх ставки: перечисли что скорее всего придётся доплатить (DTHC, таможня, последняя миля и т.д.) с примерными суммами если можешь оценить.

Ориентировочная итоговая стоимость под ключ: [ставка] + [доп расходы] ≈ [итог в USD]. Это главная цифра для сравнения.


Пиши по-русски. Без звёздочек, решёток, дефисов-маркеров. Просто текст.`;

    const userPrompt = `Маршрут: ${requestInfo?.route || "не указан"}
Груз: ${requestInfo?.cargo || "не указан"}
Условия поставки: ${requestInfo?.incoterms || "не указаны"}

Получено ставок: ${quotes.length}

${quotesText}

Проведи анализ. Выбери лучшую ставку и объясни почему.`;

    const res = await fetch("https://api.openai.com/v1/chat/completions", {
      method: "POST",
      headers: {
        "Content-Type": "application/json",
        Authorization: `Bearer ${process.env.OPENAI_API_KEY}`,
      },
      body: JSON.stringify({
        model: "gpt-4o",
        messages: [
          { role: "system", content: systemPrompt },
          { role: "user", content: userPrompt },
        ],
        max_tokens: 1000,
        temperature: 0.2,
      }),
    });

    if (!res.ok) {
      const err = await res.text();
      throw new Error(`OpenAI error: ${err}`);
    }

    const data = await res.json();
    const analysis = data.choices?.[0]?.message?.content || "";

    return NextResponse.json({ analysis });
  } catch (e: any) {
    return NextResponse.json({ error: e.message }, { status: 500 });
  }
}
